#define NULL ((VOID *)0)

static unsigned char *font_glyphs = NULL;
UINT32 font_width = 0;
UINT32 font_height = 0;
static UINT32 font_bytes_per_glyph = 0;
static UINT32 font_bytes_per_line = 0;

void font_init(unsigned char *psf_data)
{
	psf2_header_t *h = (psf2_header_t *)psf_data;
	if (h->magic != PSF2_MAGIC)
	{
		return;
	}
	font_width = h->width;
	font_height = h->height;
	font_bytes_per_glyph = h->bytes_per_glyph;
	font_bytes_per_line = (h->width + 7) / 8;
	font_glyphs = psf_data + h->headersize;
}
void put_char(char c, int x, int y, UINT32 color)
{
	if ((unsigned char)c > 127) c = 0;
	unsigned char *glyph = font_glyphs + (c * font_bytes_per_glyph);

	for (UINT32 py = 0; py < font_height; py++)
	{
		UINT16 row = (glyph[py * 2] << 8) | glyph[py * 2 + 1];

		for (UINT32 px = 0; px < 16; px++)
		{
			if (row & (0x8000 >> px))
			{
				draw_pixel(x + px, y + py, color);
			}
		}
	}
}
